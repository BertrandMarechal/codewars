function pathFinder(area) {
    const mazeSplit = area.split('\n').map(x => x.split('').map(x => +x));
    const mazeLength = mazeSplit.length;
    const path = mazeSplit.map(l => l.map(x => Infinity));
    let currentX = 0, currentY = 0;
    path[currentY][currentX] = 0;

    let next = [[currentY, currentX]];

    while (next.length) {
        const nexNexts = [];
        for (let k = 0; k < next.length; k++) {
            const element = next[k];
            for (let i = -1; i < 2; i++) {
                for (let j = -1; j < 2; j++) {
                    if (i * j !== 0 || (i === 0 && j === 0)) {
                        continue;
                    }
                    if (i + element[0] > -1 &&
                        i + element[0] < mazeLength &&
                        j + element[1] > -1 &&
                        j + element[1] < mazeLength) {

                        if (path[element[0] + i][element[1] + j] === Infinity) {
                            nexNexts.push([element[0] + i, element[1] + j]);
                        }
                        path[element[0] + i][element[1] + j] = Math.min(
                            path[element[0] + i][element[1] + j],
                            Math.abs(
                                mazeSplit[element[0] + i][element[1] + j] -
                                mazeSplit[element[0]][element[1]]
                            ) +
                            path[element[0]][element[1]]
                        );
                    }
                }
            }
        }
        next = [...nexNexts];
    }

    // while (path[mazeLength - 1][mazeLength - 1] === Infinity) {

    // }
    let somethingChanged = true;
    while (somethingChanged) {
        somethingChanged = false;
        for (let i = 0; i < mazeLength; i++) {
            for (let j = 0; j < mazeLength; j++) {
                const [nz, pz, zn, zp] = [[-1, 0], [1, 0], [0, -1], [0, 1]].map(coords => {
                    let toReturn = Infinity;
                    if (i + coords[0] > -1 &&
                        i + coords[0] < mazeLength &&
                        j + coords[1] > -1 &&
                        j + coords[1] < mazeLength
                    ) {
                        toReturn = path[i + coords[0]][j + coords[1]] +
                            Math.abs(
                                mazeSplit[i + coords[0]][j + coords[1]] -
                                mazeSplit[i][j]
                            );
                    }
                    return toReturn;
                });
                const newValue = Math.min(nz, pz, zn, zp);
                // console.log(i, j, path[i][j], newValue, nz, pz, zn, zp);

                if (path[i][j] > newValue) {
                    // console.log(path);

                    path[i][j] = newValue;
                    somethingChanged = true;
                }
            }
        }
    }
    // console.log(path);
    return path[mazeLength - 1][mazeLength - 1]
}


